package org.dawciobiel.shelldialog.cli.style;

/**
 * Defines which borders should be rendered around a dialog area.
 */
public enum BorderType {

    /** Render only left and right borders. */
    BORDER_VERTICAL,

    /** Render only top and bottom borders. */
    BORDER_HORIZONTAL,

    /** Render all borders. */
    BORDER_ALL,

    /** Do not render borders. */
    BORDER_NO

}
